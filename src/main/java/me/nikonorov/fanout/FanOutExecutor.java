package me.nikonorov.fanout;

import java.util.function.Supplier;

/**
 * Центральный прикладной API для ограниченного параллельного fan-out.
 *
 * <p>Сценарии использования должны использовать этот интерфейс вместо прямого создания
 * {@link java.util.concurrent.ExecutorService}, {@link java.util.concurrent.Semaphore},
 * {@link java.lang.Thread} или ad hoc orchestration через
 * {@link java.util.concurrent.CompletableFuture}. Это удерживает virtual-thread
 * политику, лимиты на один запрос, разворачивание ошибок и
 * обработку interrupt в одной инфраструктурной реализации.</p>
 */
public interface FanOutExecutor {

    /**
     * Открывает изолированную область fan-out для одной прикладной операции.
     *
     * <p>Лимит применяется только внутри возвращенной области. Открытие нескольких
     * областей создает независимые лимиты на один запрос; оно не создает и не
     * настраивает глобальный bulkhead всего сервиса.</p>
     *
     * @param maxParallelTasks максимальное количество задач, которые могут
     *                         одновременно выполняться внутри этой области;
     *                         реализации должны нормализовать значения меньше {@code 1}
     * @return область для отправки связанных параллельных задач
     */
    FanOutScope openScope(int maxParallelTasks);

    /**
     * Группа ограниченных параллельных задач, локальная для запроса.
     *
     * <p>Область намеренно легковесная и должна создаваться сценарием
     * использования для каждой входящей команды, которой нужен fan-out.</p>
     */
    interface FanOutScope {

        /**
         * Отправляет задачу в область.
         *
         * <p>Supplier может выполнять блокирующий ввод-вывод. Реализация решает,
         * когда задача может стартовать, исходя из доступной емкости области.</p>
         *
         * @param supplier тело выполняемой задачи
         * @param <T> тип результата
         * @return handle для ожидания результата задачи
         */
        <T> FanOutTask<T> submit(Supplier<T> supplier);
    }

    /**
     * Handle отправленной fan-out задачи.
     *
     * @param <T> тип результата, производимого задачей
     */
    interface FanOutTask<T> {

        /**
         * Ожидает завершения задачи и возвращает ее результат.
         *
         * <p>Реализации должны по возможности напрямую показывать прикладные
         * ошибки, не заставляя сценарии использования обрабатывать транспортные
         * или concurrency-specific wrapper exceptions.</p>
         *
         * @return результат завершенной задачи
         */
        T join();
    }
}
